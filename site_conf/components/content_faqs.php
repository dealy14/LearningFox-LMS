<h3>FAQ &ndash; Answers for  you</h3>
<h4>Why eLearning?</h4>
  <p>ELearning provides many benefits to employers looking to deliver  quality training while maintaining a reasonable budget. ELearning allows  24/7/365 access for employees needing specific safety training which enables  them to complete their work tasks in a safe and efficient manner. Employers  have immediate access to all training records and a systematic documentation  process. Efficiencies are improved as the time spent on completing and filing  paperwork is eliminated.</p>
<h4>What is a Learning Management System?</h4>
<ul type="disc">
  <li>Commonly known as LMS, this system is used to       deliver, track and manage learning and training materials. Our LMS allows       companies to design training curriculum and schedules, manage and report       out on progress, retrieve data through a variety of reporting applications       and utilize custom documents to deliver company specific       information.&nbsp;</li>
</ul>
<h4>Can I utilize my own LMS?</h4>
  <ul>
    <li>Yes. Our course curriculum can be set up on your existing LMS or we can  install our LMS on your servers.</li>
  </ul>
<h4>How easy is it to learn the system?</h4>
<ul type="disc">
  <li>Our LMS is easy to learn and easy to use. It is       simple to navigate and includes an online help       section which details&nbsp;the site&nbsp;functions and&nbsp;features.&nbsp;</li>
</ul>
<h4>Can I customize the site for my company's needs?</h4>
<ul type="disc">
  <li>Yes. We provide complete flexibility and       freedom. It is our belief that the effectiveness of any training program       is enhanced when the program is tailored to meet the specific needs of the       client. You can&nbsp;upload specific policies, procedures       and/or&nbsp;documents to&nbsp;personalize your site. </li>
</ul>
<h4>Are the  topics OSHA compliant?</h4>
<ul type="disc">
  <li>Yes. All subject matter follows the required       standard for the academic portion of the applicable regulations including       OSHA, DOT, EPA and JCAHO. Should certain standards require practical       (hands-on) training, that training will be available to you through Cosmos       Consulting or an accredited training agency. </li>
</ul>
